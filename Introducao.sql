-- LEFT JOIN que retorna todos os usuários e seus cargod
-- os registros da tebla da esquerda (usuario) e os registros da tabela da direita (cargo)
SELECT * FROM USUARIO LEFT JOIN CARGO ON USUARIO.ID = CARGO.FK_USUARIO; -- começa a pesquisa pela esquerda (usuario)

SELECT * FROM USUARIO RIGHT JOIN CARGO ON USUARIO.ID = CARGO.FK_USUARIO; -- começa a pesquisa pela direita (cargo)

-- Inner Join que retorna todos os registros quando houver uma correspondência entre as tabelas 
SELECT * FROM USUARIO INNER JOIN CARGO ON USUARIO.ID = CARGO.FK_USUARIO;

