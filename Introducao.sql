CREATE TABLE USUARIO (
    ID INT PRIMARY KEY NOT NULL,
    NOME VARCHAR (50),
    EMAIL VARCHAR (100)
);

CREATE TABLE CARGO (
    ID INT PRIMARY KEY NOT NULL,
    NOME VARCHAR (50),
    FK_USUARIO INT,
    CONSTRAINT FK_CARGO_USUARIO FOREIGN KEY(FK_USUARIO) REFERENCES USUARIO(ID)
);



